    <h1 style="text-align:center; font-size:200%;"><b>Day Care Center</b></h1>
    <br>
    <h3 style="margin-left:20%;">Introduction :</h3>
<p style="margin-left:20%; font-size:140%;">Rajarbag  Day Care Center is located at the ground floor of  Women Barrack of  Rajarbag Police Line.It is run by Directorate of Women Affairs under Ministry of Women and Children Affairs. It is established for the children of low income and middle income working mothers.</p>
<br>

<h3 style="margin-left:20%;">Date of  inauguration :</h3>
<p style="margin-left:20%; font-size:140%;">Rajarbag Day Car.e Center was inaugurated on 18 July, 2012 by Honourable Minister of Home Affairs Advocate Sahara Khatun at that time.</p>
<br>

<h3 style="margin-left:20%;">Main objectives :</h3>
<p style="margin-left:20%; font-size:140%;">To provide safe day care services for the children ( 06 months to 06 years) of middle income working mothers from 8.30 am to 5.30 pm.</p>
<br>
<h3 style="margin-left:20%;">Specific objectives :</h3>
<p style="margin-left:20%; font-size:140%;">
    1.To ensure middle income working mother`s services in their respective offices through providing day care services for their children.
<br>
    2.To provide food with proper nutrition,first aid,pre-school education, indoor games and entertainment for children to ensure proper mental and physical growth/development.</p>
    <br>
<br>
    <h3 style="margin-left:20%;">Seat capacity :</h3>
<p style="margin-left:20%; font-size:140%;">50 (fifty)</p>
<br>
<h3 style="margin-left:20%;">Fees for admission and monthly charges :</h3>
<p style="margin-left:20%;">Admission Fee is 250/-(Two fifty) taka and monthly charge is 300/-(Three hundred) taka</p>
<br>

<h3 style="margin-left:20%;">Children`s meal :</h3>
<p style="margin-left:20%; font-size:140%;">Children are provided with breakfast,lunch and afternoon sancks.</p>
<br>
<h3 style="margin-left:20%;">Human resourees :</h3>
<p style="margin-left:20%; font-size:140%;">Rajarbag Day-Care Center runs by 12 officials including, 01 day care officer 01 teacher for medical services,01 teacher cum computer operator,04 nursemaid,02 cooks,01 cleaner and 02 guards.<p>
