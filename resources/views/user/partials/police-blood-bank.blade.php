    <h1 style="text-align:center; ,font-size:200%;"><b>Police Blood Bank</b></h1>
    <p style="margin-left:20%; font-size:140%;">
    Police Blood Bank started its journey on the 12th December, 2010 with a unique concept of helping people by donating blood. Blood donation for saving life has created a milestone in Policing. It was actually the founding stone of turning Police force into Police Service. Blood Bank has flourished more in these years.
</p>
<br>
<h2 style="margin-left:20%;">Motive of Police Blood Bank</h2>
<p style="margin-left:20%; font-size:100%;">
1.To provide emergency blood support to police as well as public.
<br>
<br>
2.To collect blood from police blood and public blood donors.
<br>
<br>
3.To help the poor by providing emergency blood service.
<br>
<br>
4.To make a database of blood donors.
<br>
<br>
5.To support voluntary blood donation campaign.
</p>
<br>
<h2 style="margin-left:20%;">Who Can Donate Blood</h2>
<p style="margin-left:20%; font-size:100%;">
People from the age group of 18 to 60 with a body weight of 45 KG (100 lb) can normally donate blood. The donor’s body temperature should be less than 99.5° F and pulse rate should be 60-100 per minutes. Donor should have a blood pressure of permitted level. Donor’s blood should contain at least 75% of hemoglobin. He must not suffer from any skin and blood diseases and must not have any suspicious scar marks on his arms regarding drug addiction or professional blood donor.
</p>
<br>
<h2 style="margin-left:20%;">Facilities of Blood Donation</h2>
<p style="margin-left:20%; font-size:100%;">
1.A screening report of the donated blood regarding 5 blood transmitted disease.
<br>
<br>
2.She/he can get back a bag of blood of his group without any processing fees. She/he can enjoy the same facility for her/his parents, spouse or children.
<br>
<br>
3.Life time blood facility whenever required for herself/himself.
<br>
<br>
4.A souvenir (a coat pin).
<br>
<br>
5.Good Service (GS) Mark in service book.
<br>
<br>
6.Rest for 2(two) days after blood donation.
<br>
<br>
7.Donor ID Card.
<br>
<br>
8.Lifetime Membership of Police Blood Bank.
</p>
<br>
<h2 style="margin-left:20%;">Recent Activities of Blood Bank</h2>
<p style="margin-left:20%; font-size:100%;">
Blood Bank has created a mass awareness among the general people as well as among the Police regarding blood donation. Police Blood Bank has arranged many blood donation campaigns. Recently it has moved to mobile blood donation campaigns. Police blood bank has participated in more than 100 blood donation campaigns in different schools, colleges and universities. It is a very well known and prestigious brand in the field of blood donation.
</p>
